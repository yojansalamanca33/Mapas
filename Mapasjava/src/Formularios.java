    private void jbguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbguardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtnombre.getText();
        String apellido = txtapellido.getText();
        String edad = txtedad.getText();
        String telefono = txttelefono.getText();
        String correo = txtcorreo.getText();
        String asignatura = cbasignaturas.getSelectedItem().toString();
            
        String datos = apellido + " - " + edad + " a√±os - Tel: " + telefono + " - Correo: " + correo + " - Asignatura: " + asignatura;
        
         mapaEstudiantes.put(nombre, datos);
         
        txtnombre.setText("");
        txtapellido.setText("");
        txtedad.setText("");
        txttelefono.setText("");
        txtcorreo.setText("");
        cbasignaturas.setSelectedIndex(0);

        JOptionPane.showMessageDialog(this, "Datos guardados correctamente.");


    }//GEN-LAST:event_jbguardarActionPerformed

    private void jblistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jblistarActionPerformed
        // TODO add your handling code here:
         txtarea.setText(""); // limpiar area
        for (Map.Entry<String, String> entry : mapaEstudiantes.entrySet()) {
            txtarea.append("Nombre: " + entry.getKey() + " | " + entry.getValue() + "\n");
        }
    }//GEN-LAST:event_jblistarActionPerformed

